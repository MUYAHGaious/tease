  /// Helper method for glassmorphism decoration
  static BoxDecoration getGlassmorphismDecoration({
    Color? backgroundColor,
    double borderRadius = 16.0,
    double blurRadius = 10.0,
  }) {
    return BoxDecoration(
      color: backgroundColor ?? surfaceLight.withValues(alpha: 0.8),
      borderRadius: BorderRadius.circular(borderRadius),
      border: Border.all(
        color: Colors.white.withValues(alpha: 0.2),
        width: 1,
      ),
      boxShadow: [
        BoxShadow(
          color: shadowLight,
          blurRadius: blurRadius,
          offset: const Offset(0, 4),
        ),
      ],
    );
  }

  /// Helper method for data text style
  static TextStyle dataTextStyle({
    required bool isLight,
    double fontSize = 14.0,
    FontWeight fontWeight = FontWeight.w400,
    Color? color,
  }) {
    return GoogleFonts.inter(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: color ?? (isLight ? textHighEmphasisLight : textHighEmphasisDark),
      letterSpacing: 0.25,
      height: 1.43,
    );
  }

